/**
 * Select relevant few-shot examples based on input and genre hints.
 */
export function selectFewShots(
  input: string,
  genre?: string,
  count: number = 2
): FewShotExample[] {
  let candidates = FEW_SHOT_EXAMPLES;

  // If genre is specified, prioritize matching examples
  if (genre) {
    const genreLower = genre.toLowerCase();
    const matching = candidates.filter((e) =>
      e.genre.toLowerCase().includes(genreLower)
    );
    if (matching.length >= count) {
      return matching.slice(0, count);
    }
    // Mix: one matching + one diverse
    const result = matching.slice(0, 1);
    const others = candidates.filter((e) => !matching.includes(e));
    result.push(...others.slice(0, count - 1));
    return result;
  }

  // Keyword matching: pick examples relevant to input
  const keywords = extractInputKeywords(input);
  const scored = candidates.map((e) => ({
    example: e,
    score: keywords.filter((k) => e.input.includes(k) || e.genre.includes(k))
      .length,
  }));

  scored.sort((a, b) => b.score - a.score);

  if (scored[0].score > 0) {
    return scored.slice(0, count).map((s) => s.example);
  }

  // Fallback: diverse random selection
  return candidates.slice(0, count);
}

/**
 * Build a few-shot prompt string for the LLM.
 */
export function buildFewShotPrompt(
  examples: FewShotExample[],
  newInput: string,
  systemRole: string
): string {
  let prompt = `${systemRole}\n\n以下是一些"如何回应模糊输入"的优秀示例。请注意它们的模式：
- 先表达理解
- 然后给出一个 ReFrame（换个角度理解问题）
- 提供 2-3 个具体的 Goal 选项
- 用一个开放性的问题结尾

`;

  examples.forEach((ex, i) => {
    prompt += `【示例 ${i + 1} — ${ex.genre}】\n`;
    prompt += `用户输入: ${ex.input}\n`;
    prompt += `你的回复: ${ex.output}\n\n`;
  });

  prompt += `现在，请用同样的风格回复下面的新输入。`;
  prompt += `\n用户输入: ${newInput}\n你的回复:`;

  return prompt;
}

function extractInputKeywords(text: string): string[] {
  const writingKeywords = [
    "议论文", "散文", "游记", "论文", "文案", "视频",
    "公众号", "博客", "报告", "邮件", "演讲", "日记",
    "小说", "诗", "评论", "产品", "教程", "简历",
    "自由", "创作", "写作", "怎么写", "帮我写",
  ];

  return writingKeywords.filter((k) => text.includes(k));
}
